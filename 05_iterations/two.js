//While Loop
let index = 2

while (index <= 10) {
    // console.log(`Value of index ${index}`);
    index++;
}

//while Loop with array
let myArray = ['flash', 'kallu', 'sallu']
let arr = 0
while (arr < myArray.length) {
    // console.log(`Favourite ${myArray[arr]}`);
    arr++;
}

//do while loop

let score = 1
do {
    console.log(`Score is ${score}`);
    score++;
} while (score <= 10)